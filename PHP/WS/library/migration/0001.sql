CREATE DATABASE IF NOT EXISTS `HealthKeeper` CHARACTER SET "utf8";
USE `HealthKeeper`;

CREATE TABLE IF NOT EXISTS `Users` (
	`user_id` INT(11) NOT NULL AUTO_INCREMENT PRIMARY KEY,
    `firstName` VARCHAR(30) NOT NULL,
    `lastName` VARCHAR(20) NOT NULL
) DEFAULT CHARACTER SET "utf8" ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `QRCodes` (
    `qr_id` INT(11) NOT NULL AUTO_INCREMENT PRIMARY KEY,
    `md5` VARCHAR(32) NOT NULL
) DEFAULT CHARACTER SET "utf8" ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS QRMap (
    `qr_id` INT(11) NOT NULL, 
    `user_id` INT(11) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `Credentials` (
    `email` VARCHAR(80) NOT NULL PRIMARY KEY,
    `user_id` INT(11) NOT NULL,
    `password` VARCHAR(32) NOT NULL
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `GPs` (
    `gp_id` INT(11) NOT NULL AUTO_INCREMENT PRIMARY KEY,
    `firstName` VARCHAR(30) NOT NULL,
    `lastName` VARCHAR(20) NOT NULL
) DEFAULT CHARACTER SET "utf8" ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `GPSchedule` (
    `date` DATE NOT NULL,
    `gp_id` INT(11) NOT NULL,
    `email` VARCHAR(80) NOT NULL,
    `f8t9` TINYINT NOT NULL,
    `f9t10` TINYINT NOT NULL,
    `f10t11` TINYINT NOT NULL,
    `f11t12` TINYINT NOT NULL,
    `f12t13` TINYINT NOT NULL,
    `f13t14` TINYINT NOT NULL,
    `f14t15` TINYINT NOT NULL,
    `f15t16` TINYINT NOT NULL,
    `f16t17` TINYINT NOT NULL,
    `f17t18` TINYINT NOT NULL
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS `UserSchedule` (
    `date` DATE NOT NULL,
    `user_id` INT(11) NOT NULL,
    `gp_id` INT(11) NOT NULL,
    `f8t9` TINYINT NOT NULL,
    `f9t10` TINYINT NOT NULL,
    `f10t11` TINYINT NOT NULL,
    `f11t12` TINYINT NOT NULL,
    `f12t13` TINYINT NOT NULL,
    `f13t14` TINYINT NOT NULL,
    `f14t15` TINYINT NOT NULL,
    `f15t16` TINYINT NOT NULL,
    `f16t17` TINYINT NOT NULL,
    `f17t18` TINYINT NOT NULL
) ENGINE=InnoDB;
